-- Four design questions are answered in comments below before the CREATE TABLE.
-- Review each decision before marking this migration ready to execute.

-- ─── DESIGN QUESTION (a): verification_status — store or compute on read? ────
--
-- RECOMMENDATION: COMPUTE ON READ. Do NOT add a verification_status column.
--
-- Reason 1 — append-only invariant: verification_status is the result of
--   HMAC(secret, signing_payload) === signature. That is, it is derived entirely from
--   data already in the row plus the live secret. Storing it would mean storing a
--   conclusion that can be recomputed in O(1) from what is already there.
--
-- Reason 2 — mutability violation: if stored, a subsequent verify call would want to
--   UPDATE the row (flip status from null → 'valid'). UPDATE is not append-only.
--   The row would need both a verified_at timestamp and a mutable status field —
--   that is a mutable audit log, not an immutable ledger.
--
-- Reason 3 — security: a stored status is a tampering target. An attacker who can
--   write `verification_status = 'valid'` without touching signature/payload fakes
--   cleanliness. Computing status from signature+payload on every verify call removes
--   this attack surface entirely.
--
-- Verdict: status is computed by the verify endpoint, never stored.

-- ─── DESIGN QUESTION (b): PK choice — verdict_id or separate row id? ─────────
--
-- RECOMMENDATION: SEPARATE ROW ID (UUID generated by Worker before INSERT).
--   verdict_id is indexed but NOT the PK.
--
-- Reason: verdict_id = SHA-256(VERDICT_ID_VERSION | signal.id | source |
--   source_type | published_at | decision | sourceProfileId | intentProfile).
--   It does NOT include evaluated_at. The same signal evaluated twice with the same
--   inputs yields the same verdict_id but represents two distinct, honest ledger entries.
--   If verdict_id were the PK, the second INSERT would collide and either fail or
--   silently replace a row — destroying evaluation history.
--
-- The ledger's value is partly in showing re-evaluation history: "I judged this three
--   times and always got 'use_first'" or "June said 'needs_refresh', July said 'use_first'
--   after the source refreshed" — both honest, both valuable. A unique verdict_id PK
--   cannot represent this.
--
-- A separate UUID-style id (generated by Worker, e.g. crypto.randomUUID()) is the PK.
--   A non-unique index on verdict_id serves fast lookup for the verify endpoint.
--
-- Optional dedup guard (for consideration, NOT in this file): a UNIQUE constraint on
--   (verdict_id, evaluated_at) would prevent byte-identical duplicate rows without
--   blocking legitimate re-evaluations at different times. Left open for discussion.

-- ─── DESIGN QUESTION (c): canonical_content_sha256 — needed in the row? ──────
--
-- RECOMMENDATION: YES, store as a separate column with its own index.
--
-- Reason 1 — verify does not need it directly: the verify endpoint reads signing_payload
--   (already stored) and recomputes HMAC(secret, signing_payload). It does not need to
--   re-derive the content hash. So in the narrow verify path, this column is redundant.
--
-- Reason 2 — queryability: "has this content appeared before in our ledger?" requires
--   scanning signing_payload as a substring match without it — O(n) full-table scan.
--   A separate indexed column makes this O(log n).
--
-- Reason 3 — analytics: "find all evaluations for this specific content hash" or
--   "how many unique content hashes did we evaluate this month?" are queries that
--   become natural with this column and impossible without it.
--
-- Cost: one TEXT column (64 hex chars). Negligible. Worth it.

-- ─── DESIGN QUESTION (d): indexes ────────────────────────────────────────────
--
-- The verify endpoint's lookup path is by verdict_id.
-- The human audit path is by result_id (source result) or evaluated_at (time range).
-- The content dedup path is by canonical_content_sha256.
--
-- Four indexes proposed (see CREATE INDEX statements below):
--   1. verdict_id — primary verify lookup: "all snapshots for this verdict"
--   2. result_id — source result lookup: "all evaluations of this source"
--   3. evaluated_at — time-ordered queries: "evaluations in the last N days"
--   4. canonical_content_sha256 — content dedup: "have we seen this content?"
--
-- id is the PK so it is automatically indexed by SQLite.

-- ─── TABLE ────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS evaluation_snapshots (
  -- Row identity
  id TEXT PRIMARY KEY,
  -- ^ UUID generated by Worker before INSERT (crypto.randomUUID()).
  --   NOT verdict_id — see design question (b).

  -- Verdict identity (the "what" of this evaluation)
  verdict_id TEXT NOT NULL,
  -- ^ SHA-256(VERDICT_ID_VERSION | signal.id | source | source_type | published_at |
  --   decision | sourceProfileId | intentProfile). Deterministic. NOT unique in this
  --   table — same verdict can be evaluated multiple times; each is its own row.

  result_id TEXT,
  -- ^ provenance_readiness.source_identity.result_id. Nullable: not all signals yield
  --   a stable result_id (bare-text signals without a URL source may not).

  -- Signal identity fields (stored for human audit + verify fallback context)
  signal_source TEXT NOT NULL,
  -- ^ signal.source URL. Part of verdict_id basis; useful for human review.

  signal_source_type TEXT NOT NULL,
  -- ^ signal.source_type (adapter name, e.g., 'arxiv'). Part of verdict_id basis.

  signal_published_at TEXT,
  -- ^ signal.published_at (ISO-8601). Nullable. Part of verdict_id basis.

  -- Decision (the "what FreshContext decided")
  decision TEXT NOT NULL,
  -- ^ ContextDecision enum value (e.g., 'use_first', 'exclude'). Machine-readable.
  --   Part of verdict_id basis.

  decision_label TEXT NOT NULL,
  -- ^ Human-readable label (e.g., 'Use first', 'Exclude'). Computed from decision
  --   at eval time. Stored so verify can surface it without re-deriving.

  -- Profile context
  source_profile_id TEXT,
  -- ^ Source profile applied (e.g., 'academic_research'). Nullable: default profile
  --   has no id. Part of verdict_id basis.

  intent_profile_id TEXT,
  -- ^ Intent profile applied (e.g., 'citation_check'). Nullable. Part of verdict_id basis.

  -- Temporal anchors (STORED eval-time values — never recomputed on read)
  evaluated_at TEXT NOT NULL,
  -- ^ ISO-8601 from ContextDecisionResult.evaluated_at. The "when FreshContext judged
  --   this" anchor. INVARIANT: this is the stored value, never the live clock.

  revalidate_after TEXT,
  -- ^ ISO-8601 derived from source profile decay at evaluation time. Nullable (some
  --   sources have no configured revalidation window). STORED VALUE.

  -- Signing metadata
  engine_version TEXT NOT NULL,
  -- ^ SERVICE_VERSION at sign time (e.g., '0.3.23'). CRITICAL — signatures are
  --   VERSION-SCOPED. The verify endpoint MUST feed the stored value back into payload
  --   recompute, never the current SERVICE_VERSION constant. A version bump changes
  --   every signature; this is the anchor that keeps old rows verifiable.

  canonical_content_sha256 TEXT,
  -- ^ SHA-256 of canonicalized content. Nullable (signals with no content cannot have
  --   a content hash and therefore cannot be signed; they should not appear in this
  --   table, but the nullable guard is defensive).
  --   Stored separately from signing_payload for queryability — see design question (c).

  signing_payload TEXT NOT NULL,
  -- ^ Full buildHaPriPayload (v3: adds decision + verdict_id to v2's 8 fields) string.
  --   Verify reads this directly: HMAC(secret, signing_payload) must equal signature.
  --   Append-only — this is the eval-time snapshot of what was signed.

  signature TEXT NOT NULL,
  -- ^ HMAC-SHA256 hex (64 lowercase chars) from the [FRESHCONTEXT_SIG_V1] block.
  --   The verify endpoint recomputes HMAC(secret, signing_payload) and checks equality.

  signature_version TEXT NOT NULL DEFAULT 'FRESHCONTEXT_HA_PRI_V3',
  -- ^ Version tag. 'FRESHCONTEXT_HA_PRI_V3' for Pass 23 payload (v2 fields + decision +
  --   verdict_id). DEFAULT locks new rows to the current version; explicit INSERT can
  --   specify a different version for future schema evolution without a migration.

  -- Ledger housekeeping
  created_at TEXT NOT NULL DEFAULT (datetime('now'))
  -- ^ Row creation timestamp. NOT the evaluation time (evaluated_at carries that).
  --   Tracks when the row was written to D1, not when FreshContext made the decision.
);

-- ─── INDEXES ──────────────────────────────────────────────────────────────────

-- Primary verify lookup: find all snapshots for a given verdict
CREATE INDEX IF NOT EXISTS idx_eval_snapshots_verdict_id
  ON evaluation_snapshots (verdict_id);

-- Source result lookup: all evaluations of a specific source result
CREATE INDEX IF NOT EXISTS idx_eval_snapshots_result_id
  ON evaluation_snapshots (result_id);

-- Time-ordered queries: evaluations in a date range
CREATE INDEX IF NOT EXISTS idx_eval_snapshots_evaluated_at
  ON evaluation_snapshots (evaluated_at);

-- Content dedup: have we seen this content hash before?
CREATE INDEX IF NOT EXISTS idx_eval_snapshots_content_sha
  ON evaluation_snapshots (canonical_content_sha256);
