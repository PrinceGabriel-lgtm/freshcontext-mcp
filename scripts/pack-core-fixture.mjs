#!/usr/bin/env node
import { cp, mkdir, readFile, readdir, rm, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import { join } from "node:path";

const ROOT = process.cwd();
const CORE_DIST = join(ROOT, "dist", "core");
const FIXTURE_ROOT = join(ROOT, "tmp", "freshcontext-core-fixture");
const FIXTURE_DIST = join(FIXTURE_ROOT, "dist");
const FIXTURE_COMPAT = join(FIXTURE_DIST, "compat");
const FIXTURE_DOCS = join(FIXTURE_ROOT, "docs");
const FIXTURE_VERSION = "0.3.21-fixture.0";

const coreFiles = [
  "decay",
  "decision",
  "envelope",
  "explain",
  "guards",
  "index",
  "pipeline",
  "provenance",
  "rank",
  "readable",
  "signal",
  "sourceProfiles",
  "types",
  "utility",
];

const docs = [
  "CORE_API.md",
  "SIGNAL_CONTRACT.md",
  "SOURCE_PROFILES.md",
  "HA_PRI_V2_DESIGN.md",
];

const legalFiles = [
  "LICENSE",
  "NOTICE.md",
  "SECURITY.md",
  "TRADEMARKS.md",
];

function assertExists(path, label) {
  if (!existsSync(path)) {
    console.error(`Missing ${label}: ${path}`);
    console.error("Run `npm run build` before generating the Core fixture.");
    process.exit(1);
  }
}

async function copyRequiredFile(from, to) {
  assertExists(from, from);
  await cp(from, to);
}

async function assertNoForbiddenFiles() {
  const forbidden = [
    "dist/server.js",
    "dist/server.d.ts",
    "dist/apify.js",
    "dist/apify.d.ts",
    "dist/adapters",
    "dist/rest",
    "dist/tools",
    "src",
    "worker",
    "examples",
    "tests",
    "scripts",
    "node_modules",
  ];

  const entries = await readdir(FIXTURE_ROOT, { recursive: true });
  const normalized = entries.map((entry) => String(entry).replace(/\\/g, "/"));
  const matches = normalized.filter((entry) => forbidden.some((path) => entry === path || entry.startsWith(`${path}/`)));

  if (matches.length > 0) {
    console.error("Generated Core fixture contains forbidden files:");
    for (const match of matches) console.error(`- ${match}`);
    process.exit(1);
  }
}

function packageJson() {
  return {
    name: "freshcontext-core",
    version: FIXTURE_VERSION,
    description: "Dependency-light context judgment engine for AI agents and retrieval systems.",
    license: "MIT",
    type: "module",
    engines: {
      node: ">=20",
    },
    main: "./dist/index.js",
    types: "./dist/index.d.ts",
    exports: {
      ".": {
        types: "./dist/index.d.ts",
        import: "./dist/index.js",
      },
      "./compat": {
        types: "./dist/compat/index.d.ts",
        import: "./dist/compat/index.js",
      },
      "./package.json": "./package.json",
    },
    files: [
      "dist/",
      "docs/",
      "README.md",
      "LICENSE",
      "NOTICE.md",
      "SECURITY.md",
      "TRADEMARKS.md",
    ],
    dependencies: {},
  };
}

function readme() {
  return `# FreshContext Core Fixture

This generated fixture proves the future dependency-light \`freshcontext-core\` package boundary.

It is not a published package and not a release candidate.

## Imports

\`\`\`ts
import { evaluateSignals, interpretEvaluations, getSourceProfile } from "freshcontext-core";
import { formatForLLM } from "freshcontext-core/compat";
\`\`\`

## Boundary

- No MCP server
- No reference adapters
- No Worker runtime
- No REST handler
- No runtime dependencies

Generated by \`scripts/pack-core-fixture.mjs\`.
`;
}

async function main() {
  assertExists(join(CORE_DIST, "index.js"), "built Core index");
  assertExists(join(CORE_DIST, "index.d.ts"), "built Core declarations");

  await rm(FIXTURE_ROOT, { recursive: true, force: true });
  await mkdir(FIXTURE_COMPAT, { recursive: true });
  await mkdir(FIXTURE_DOCS, { recursive: true });

  for (const name of coreFiles) {
    await copyRequiredFile(join(CORE_DIST, `${name}.js`), join(FIXTURE_DIST, `${name}.js`));
    await copyRequiredFile(join(CORE_DIST, `${name}.d.ts`), join(FIXTURE_DIST, `${name}.d.ts`));
  }

  await writeFile(
    join(FIXTURE_COMPAT, "index.js"),
    [
      'export { stampFreshness, formatForLLM, toStructuredJSON } from "../envelope.js";',
      "",
    ].join("\n")
  );
  await writeFile(
    join(FIXTURE_COMPAT, "index.d.ts"),
    [
      'export { stampFreshness, formatForLLM, toStructuredJSON } from "../envelope.js";',
      'export type { FreshContext, ExtractOptions, AdapterResult, EnvelopeFormatOptions } from "../types.js";',
      "",
    ].join("\n")
  );

  for (const doc of docs) {
    await copyRequiredFile(join(ROOT, "docs", doc), join(FIXTURE_DOCS, doc));
  }

  for (const file of legalFiles) {
    await copyRequiredFile(join(ROOT, file), join(FIXTURE_ROOT, file));
  }

  await writeFile(join(FIXTURE_ROOT, "README.md"), readme());
  await writeFile(join(FIXTURE_ROOT, "package.json"), `${JSON.stringify(packageJson(), null, 2)}\n`);

  await assertNoForbiddenFiles();

  const manifest = JSON.parse(await readFile(join(FIXTURE_ROOT, "package.json"), "utf8"));
  console.log(`Generated ${manifest.name}@${manifest.version}`);
  console.log(FIXTURE_ROOT);
  console.log("");
  console.log("Next validation commands:");
  console.log("  cd tmp/freshcontext-core-fixture");
  console.log("  npm pack --dry-run --json");
  console.log("  npm pack");
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
